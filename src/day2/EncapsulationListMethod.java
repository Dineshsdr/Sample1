package day2;

public class EncapsulationListMethod {

	public int getEmpId() {
		return EmpId;
	}
	public void setEmpId(int empId) {
		EmpId = empId;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public long getPhNo() {
		return PhNo;
	}
	public void setPhNo(long phNo) {
		PhNo = phNo;
	}
	private int EmpId;
	private String Name;
	private long PhNo;
	
	
	
}
